/**
 * Function qui permet de faire le carré du nombre entré en paramètre
 * @param {number} num
 * @returns {number} carré de num
 */
export function auCarre(num: number): number {
    return num * num
}

/**
 * Function qui permet de faire le cube du nombre entré en paramètre
 * @param {number} num
 * @returns {number} cube de num
 */
export function auCube(num: number): number {
    return num * num * num
}



